/// <summary>
/// Provides a way to control how email is sent to subscribers.
/// </summary>
export enum EmailSendToName {
  /// <summary>
  /// Who to send the email to.
  /// </summary>
  To = 'To',
  /// <summary>
  /// Carbon copy
  /// </summary>
  CC = 'CC',
  /// <summary>
  /// Blind carbon copy
  /// </summary>
  BCC = 'BCC',
}

namespace TNO.Entities;

/// <summary>
/// Provides value types which control what users can enter for data.
/// </summary>
public enum ValueType
{
    /// <summary>
    /// Boolean values.
    /// </summary>
    Boolean = 0,

    /// <summary>
    /// String values.
    /// </summary>
    String = 1,

    /// <summary>
    /// Text values.
    /// </summary>
    Text = 2,

    /// <summary>
    /// Numeric values.
    /// </summary>
    Numeric = 3
}
